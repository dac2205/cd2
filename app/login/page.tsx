"use client";

import React, { useEffect } from "react";
import { useAuth } from "@/lib/auth";
import { useRouter } from "next/navigation";
import { Card, CardContent, CardHeader, CardTitle, CardDescription } from "@/components/ui/Card";
import { Lock } from "lucide-react";
import { FEATURES } from "@/lib/config/features";

export default function LoginPage() {
    const { login } = useAuth();
    const router = useRouter();

    // Redirect to homepage if auth is disabled
    useEffect(() => {
        if (!FEATURES.AUTH_ENABLED) {
            router.push("/");
        }
    }, [router]);

    // Show message if auth is disabled
    if (!FEATURES.AUTH_ENABLED) {
        return (
            <div style={{
                minHeight: "100vh",
                display: "flex",
                alignItems: "center",
                justifyContent: "center",
                backgroundColor: "hsl(var(--background))",
                padding: "1rem"
            }}>
                <Card style={{ width: "100%", maxWidth: "400px" }}>
                    <CardHeader style={{ textAlign: "center" }}>
                        <CardTitle>Authentication Disabled</CardTitle>
                        <CardDescription>Authentication is currently disabled. Redirecting to homepage...</CardDescription>
                    </CardHeader>
                </Card>
            </div>
        );
    }

    const handleGoogleLogin = () => {
        login();
    };

    return (
        <div style={{
            minHeight: "100vh",
            display: "flex",
            alignItems: "center",
            justifyContent: "center",
            backgroundColor: "hsl(var(--background))",
            padding: "1rem"
        }}>
            <Card style={{ width: "100%", maxWidth: "400px" }}>
                <CardHeader style={{ textAlign: "center" }}>
                    <div style={{
                        margin: "0 auto 1rem",
                        width: "3rem",
                        height: "3rem",
                        backgroundColor: "hsl(var(--primary) / 0.1)",
                        borderRadius: "50%",
                        display: "flex",
                        alignItems: "center",
                        justifyContent: "center",
                        color: "hsl(var(--primary))"
                    }}>
                        <Lock size={24} />
                    </div>
                    <CardTitle>Đăng nhập</CardTitle>
                    <CardDescription>Đăng nhập bằng tài khoản Google để tiếp tục.</CardDescription>
                </CardHeader>
                <CardContent>
                    <button
                        onClick={handleGoogleLogin}
                        style={{
                            width: "100%",
                            padding: "0.75rem",
                            backgroundColor: "hsl(var(--primary))",
                            color: "white",
                            border: "none",
                            borderRadius: "0.375rem",
                            fontWeight: 500,
                            cursor: "pointer",
                            display: "flex",
                            alignItems: "center",
                            justifyContent: "center",
                            gap: "0.5rem"
                        }}
                    >
                        <svg className="w-5 h-5" viewBox="0 0 24 24" width="20" height="20">
                            <path
                                d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                                fill="#4285F4"
                            />
                            <path
                                d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                                fill="#34A853"
                            />
                            <path
                                d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                                fill="#FBBC05"
                            />
                            <path
                                d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                                fill="#EA4335"
                            />
                        </svg>
                        Sign in with Google
                    </button>
                </CardContent>
            </Card>
        </div>
    );
}
